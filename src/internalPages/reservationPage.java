    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked

       
             int rowlndex = customer_table.getSelectedRow();
if (rowlndex < 0) {
    JOptionPane.showMessageDialog(null, "Please Select an Item!");
} else {
    TableModel model = customer_table.getModel();
    int columnCount = model.getColumnCount();
    if (columnCount >= 4) {
        reservationForm stf = new reservationForm();
        stf.rv_id.setText("" + model.getValueAt(rowlndex, 0));
        stf.rv_address.setText("" + model.getValueAt(rowlndex, 1));
        stf.rv_amount.setText("" + model.getValueAt(rowlndex, 2));
        stf.status = model.getValueAt(rowlndex, 3).toString();

        String gend = model.getValueAt(rowlndex, 3).toString();
        if (gend.equals("Male")) {
            stf.male.setSelected(true);
        }
        if (gend.equals("Female")) {
            stf.female.setSelected(true);
        }

        stf.rv_type.setSelectedItem(model.getValueAt(rowlndex, 4).toString());

        stf.action = "Update";
        stf.rv_label.setText("UPDATE");
        stf.setVisible(true);

        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        mainFrame.dispose();
    } else {
        // Handle the case where the table model does not have enough columns
        JOptionPane.showMessageDialog(null, "Invalid number of columns in the table model!");
    }
}
   

        
        
       


    }//GEN-LAST:event_editMouseClicked

